/*

Tema: Concatenar cadenas de caracteres.
*/

var nombreCompleto = "Alan" + "Turing"; // Sin espacio entre las cadenas
console.log(nombreCompleto);

nombreCompleto = "Alan " + "Turing"; // Con espacio
console.log(nombreCompleto);

nombreCompleto = "Alan" + " Turing"; // Con espacio
console.log(nombreCompleto);

nombreCompleto = "Alan" + " " + "Turing"; // Con espacio
console.log(nombreCompleto);
